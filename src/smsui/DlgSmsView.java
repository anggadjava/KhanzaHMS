    private void BtnServiceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnServiceActionPerformed
        try {
           koneksi = MYSQLConnection.buatKoneksi();
        } catch (Exception e) {
            panelStatus1.setStatusKoneksi(PanelStatus.KONEKSI_ERROR,"Error saat Koneksi ke database");
            return;
        }

        if(koneksi == null){
            return;
        }

        SMSReadService smsService = new SMSReadService( koneksi,logArea);
        smsService.setStatus(status);
        try {
            smsService.startService();
            panelIndikator1.setStatus(status);
            panelStatus1.setStatusKoneksi(PanelStatus.TERSAMBUNG,"Service tersedia");
        } catch (Exception ex) {
            panelStatus1.setStatusKoneksi(PanelStatus.TERSAMBUNG,"Error saat mulai Service");
            System.out.println("error : "+ex);
        }
 
    }//GEN-LAST:event_BtnServiceActionPerformed

    private void BtnPesanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnPesanActionPerformed
        if(koneksi == null ){
            JOptionPane.showMessageDialog(this, "Start Service terlebih dahulu!!");
            return;
        }
        
        dlgPesan.setKoneksi(koneksi);
        dlgPesan.setVisible(true);
    }//GEN-LAST:event_BtnPesanActionPerformed

