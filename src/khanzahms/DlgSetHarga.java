    public void tampil() {
        String sql="select distributor,grosir,retail from setpenjualan";
        prosesCari(sql);
    }

    private void prosesCari(String sql) {
        Valid.tabelKosong(tabMode);
        try{
            java.sql.Statement stat=koneksi.createStatement();
            ResultSet rs=stat.executeQuery(sql);
            while(rs.next()){
                tabMode.addRow(new Object[]{rs.getString(1),rs.getString(2),rs.getString(3)});
             }
        }catch(SQLException e){
            System.out.println("Error : "+e);
        }
    }

    private void getData() {
        int row=tbAdmin.getSelectedRow();
        if(row!= -1){
            distrib.setText(tabMode.getValueAt(row,0).toString());
            grosir.setText(tabMode.getValueAt(row,1).toString());
            retail.setText(tabMode.getValueAt(row,2).toString());
        }
    }

    public void emptTeks() {
        distrib.setText("0");
        grosir.setText("0");
        retail.setText("0");
        distrib.requestFocus();
    }
