    private void BtnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnSimpanActionPerformed
        if(distrib.getText().trim().equals("")){
            Valid.textKosong(distrib,"Harga Distributor");
        }else if(grosir.getText().trim().equals("")){
            Valid.textKosong(grosir,"Harga Grosir");
        }else if(retail.getText().trim().equals("")){
            Valid.textKosong(retail,"Harga Retail");
        }else if(tabMode.getRowCount()==0){
            Sequel.menyimpan("setpenjualan","'"+distrib.getText()+"','"+grosir.getText()+"','"+retail.getText()+"'","Set Harga");
            tampil();
            emptTeks();
        }else if(tabMode.getRowCount()>0){
            JOptionPane.showMessageDialog(null,"Maaf, Hanya diijinkan satu record ...!!!!");
            distrib.requestFocus();
        }
}//GEN-LAST:event_BtnSimpanActionPerformed

